/**
  * @brief This function handles EXTI line[15:10] interrupts.
  */


/* USER CODE BEGIN 1 */
void EXTI15_10_IRQHandler(void)
{
  if (EXTI->PR & (1U << 13U)) {
    EXTI->PR = (1U << 13U); // pending clear

    uint32_t now = HAL_GetTick();
    if ((now - g_btn_last_ms) < BTN_DEBOUNCE_MS) return;
    g_btn_last_ms = now;

    if (g_led_state == LED_STATE_OFF) {
      g_led_state = LED_STATE_ON;
      GPIOA->BSRR = (1U << 5);              // LED ON

    } else if (g_led_state == LED_STATE_ON) {
      g_led_state = LED_STATE_BLINK;
      GPIOA->BSRR = (1U << (5U + 16U));     // BLINK 진입 기준 OFF
      g_tick10ms = 0;

    } else {
      g_led_state = LED_STATE_OFF;
      GPIOA->BSRR = (1U << (5U + 16U));     // LED OFF
    }
  }
}


void TIM2_IRQHandler(void)
{
  if (TIM2->SR & TIM_SR_UIF) {
    TIM2->SR &= ~TIM_SR_UIF; // clear UIF

    g_tick10ms++;

    if (g_led_state == LED_STATE_OFF) {
      GPIOA->BSRR = (1U << (5U + 16U));
    } else if (g_led_state == LED_STATE_ON) {
      GPIOA->BSRR = (1U << 5U);
    } else { // BLINK
      if ((g_tick10ms % BLINK_TOGGLE_TICKS) == 0U) {
        GPIOA->ODR ^= (1U << 5U);
      }
    }
  }
}

